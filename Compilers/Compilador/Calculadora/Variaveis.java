public class Variaveis {
	private String Tipo;
	private String Nome;
	private String Valor;

	public void setTipo(String t)
	{
		this.Tipo = t;
	}

	public void setNome(String n)
	{
		this.Nome = n;
	}

	public void setValor(String v)
	{
		this.Valor = v;
	}

	public String getTipo()
	{
		return this.Tipo;
	}

	public String getNome()
	{
		return this.Nome;
	}

	public String getValor()
	{
		return this.Valor;
	} 
}